// Function to pause NFT contract
async function pause(fromAddress, privateKey) {
    const tx = NFTContarct.methods.pause();
    const pause_response = await sendTransaction(tx, fromAddress, privateKey);
    return pause_response;
}

// Function to unpause or resume the NFT Contract
async function unpause(fromAddress, privateKey) {
    const tx = NFTContarct.methods.unpause();
    const un_pause_response = await sendTransaction(tx, fromAddress, privateKey);
    return un_pause_response;
}

// Function to mint NFT to video uploader and viewer
async function mintNft(recipient, category, tokenURI, fromAddress, privateKey) {
    const tx = NFTContarct.methods.mintNft(recipient, category, tokenURI);
    const mint_nft_response = await sendTransaction(tx, fromAddress, privateKey);
    return mint_nft_response;
}

// Function to transfer NFT from one user to another
async function transfer(tokenId, recipient, fromAddress, privateKey) {
    const tx = NFTContarct.methods.transfer(tokenId, recipient);
    const transfer_response= await sendTransaction(tx, fromAddress, privateKey);
    return transfer_response;
}

// Function to approve an nft to an address
async function approve(to, tokenId, fromAddress, privateKey) {
    const tx = NFTContarct.methods.approve(to, tokenId);
    const approve_response = await sendTransaction(tx, fromAddress, privateKey);
    return approve_response;
}

// Function to transfer approved NFT tokens 
async function transferFrom(from, to, tokenId, fromAddress, privateKey) {
    const tx = NFTContarct.methods.transferFrom(from, to, tokenId);
    const transfer_from_response = await sendTransaction(tx, fromAddress, privateKey);
    return transfer_from_response;
}

